#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char cc;
	unsigned char *ptr;

	cc = (unsigned char)c;
	ptr = (unsigned char *)b;
	while (len--)
		*ptr++ = cc;
	return (b);
}
