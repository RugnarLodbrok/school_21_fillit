#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		n;
	char	*ret;

	n = ft_strlen(s1);
	if (!(ret = malloc(sizeof(char) * (n + 1))))
		return (0);
	ft_strcpy(ret, s1);
	return (ret);
}
