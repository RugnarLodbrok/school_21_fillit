#include <stdlib.h>

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char *ret;
	char *ptr;

	if (!(ret = malloc(sizeof(char) * (len + 1))))
		return (0);
	ptr = ret;
	s += start;
	while (len--)
		*ptr++ = *s++;
	*ptr = 0;
	return (ret);
}
