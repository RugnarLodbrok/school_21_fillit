#include <stdlib.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *d;

	d = dst;
	while (len-- && *src)
		*d++ = *src++;
	if (len++)
		while (len--)
			*d++ = 0;
	return (dst);
}
