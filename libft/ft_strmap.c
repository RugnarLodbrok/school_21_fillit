#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char *ret;
	char *ptr;

	if (!(ret = ft_strdup(s)))
		return (0);
	ptr = ret;
	while (*s)
		*ptr++ = f(*s++);
	return (ret);
}
