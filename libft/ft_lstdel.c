#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *next;

	while (1)
	{
		next = (*alst)->next;
		ft_lstdelone(alst, del);
		if (next)
			*alst = next;
		else
			break ;
	}
}
