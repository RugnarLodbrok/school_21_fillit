#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*ret;
	unsigned int	i;

	i = 0;
	if (!(ret = ft_strdup(s)))
		return (0);
	while (*s)
	{
		ret[i] = f(i, *s++);
		i++;
	}
	return (ret);
}
